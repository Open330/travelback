// ── Translation key type ──
export type TranslationKey = keyof typeof translations.en

// ── Detect browser locale ──
export function detectLocale(): Locale {
  if (typeof navigator === 'undefined') return 'en'
  const lang = navigator.language || ''
  if (lang === 'ko-KR' || lang === 'ko') return 'ko'
  if (lang.startsWith('ja')) return 'ja'
  if (lang.startsWith('zh')) return 'zh'
  if (lang.startsWith('es')) return 'es'
  return 'en'
}

// ── Translation function ──
export function t(key: TranslationKey, locale: Locale = 'en'): string {
  return translations[locale][key] || translations.en[key] || key
}

// ── React Context ──
export interface LocaleContextValue {
  locale: Locale
  setLocale: (l: Locale) => void
  t: (key: TranslationKey) => string
}

export const LocaleContext = createContext<LocaleContextValue>({
  locale: 'en',
  setLocale: () => {},
  t: (key) => translations.en[key] || key,
})

const LOCALE_STORAGE_KEY = 'travelback-locale'
const VALID_LOCALES: Locale[] = ['en', 'ko', 'ja', 'zh', 'es']

function getInitialLocale(): Locale {
  if (typeof window === 'undefined') return 'en'

  try {
    const stored = localStorage.getItem(LOCALE_STORAGE_KEY) as Locale | null
    if (stored && VALID_LOCALES.includes(stored)) {
      return stored
    }
  } catch {}

  return detectLocale()
}

// ── Provider component ──
export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => getInitialLocale())

  useEffect(() => {
    document.documentElement.setAttribute('lang', locale)
  }, [locale])

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l)
    document.documentElement.setAttribute('lang', l)
    try { localStorage.setItem(LOCALE_STORAGE_KEY, l) } catch { /* ignore */ }
  }, [])

  const translate = useCallback((key: TranslationKey) => {
    return t(key, locale)
  }, [locale])

  return createElement(
    LocaleContext.Provider,
    { value: { locale, setLocale, t: translate } },
    children
  )
}

// ── Hook ──
export function useLocale() {
  return useContext(LocaleContext)
}
